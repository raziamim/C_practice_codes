#include <stdio.h>
#include <stdlib.h>

int main()
{
    const int A = 25, B = 15;
    int c = 18;
    printf("First variable is: %d\n",A);
    printf("Second variable is: %d\n",B);
    printf("Third variable is: %d\n",c);
    printf("Sum of three variables: %d\n",A+B+c);
    printf("Multiplication of three variables is: %d",A*B*c);
    return 0;
}
